  games_results <- reactive({
    # Filter to days and times
    games <- subset(games, dow %in% days())
    games <- subset(games, hod >= hod_start() & hod <= hod_end())
    
    #Filter for Paid Status
    if(("free" %in% game_types()) & ("paid" %in% game_types())) {
      games <- games
    } else if(("free" %in% game_types()) & !("paid" %in% game_types())) {
      games <- subset(games, paid == FALSE)
    } else if(!("free" %in% game_types()) & ("paid" %in% game_types())) {
      games <- subset(games, paid == TRUE)
    } else {
      games <- games
    }
    
    if(("oneshot" %in% game_types()) & ("recurring" %in% game_types())) {
      games <- games
    } else if(("oneshot" %in% game_types()) & !("recurring" %in% game_types())) {
      games <- subset(games, one_shot == TRUE)
    } else if(!("oneshot" %in% game_types()) & ("recurring" %in% game_types())) {
      games <- subset(games, one_shot == FALSE)
    } else {
      games <- games
    }
    
    com <- abs(games$combat - playerScores()$combat)
    puz <- abs(games$puzzle - playerScores()$puzzle)
    soc <- abs(games$social - playerScores()$social)
    ps <- abs(games$playerstory - playerScores()$playerstory) 
    rp <- abs(games$roleplay - playerScores()$roleplay)
    ru <- abs(games$rules - playerScores()$rules)
    
    weights <- data.frame(
      "com_w" = 1,
      "puz_w" = 1,
      "soc_w" = 1,
      "ps_w" = 1,
      "rp_w" = 1,
      "ru_w" = 1)
    
    total_score <- com*weights$com_w + puz*weights$puz_w + soc*weights$soc_w + ps*weights$ps_w + rp*weights$rp_w + ru*weights$ru_w
    
    
    avg_score <- total_score/6
    
    games["player_score"] <- 100-round(avg_score, 1)
    games[order(games$player_score, decreasing = TRUE),]
  })
