  game_types <- reactive({input$types})
  
  playerScores <- reactive({
    data.frame(
      combat =  input$comb,
      puzzle = input$puzz,
      social = input$soci,
      playerstory = input$stry,
      roleplay = input$role,
      rules = input$rule
    )
  })
  
  output$player_scores <- renderTable({
