  generate_games <- function(
    game_count = 1000, 
    paid_pct = 0.9,
    one_shot_pct = 0.9)
  {
    game_id <- 1:game_count
    
    scores_experience <- sample(4, game_count, replace = TRUE)
    scores_ranking <- sample(50, game_count, replace = TRUE) / 10
    
    scores_combat <- sample(100, game_count, replace = TRUE)
    scores_puzzles <- sample(100, game_count, replace = TRUE)
    scores_social <- sample(100, game_count, replace = TRUE)
    scores_playerstory <- sample(100, game_count, replace = TRUE)
    scores_roleplaying <- sample(100, game_count, replace = TRUE)
    scores_rules <- sample(100, game_count, replace = TRUE)
    
    scores_paid <- sample(c(FALSE, TRUE), game_count, replace = TRUE, prob = c(paid_pct, 1-paid_pct))
    
    day_of_week <- sample(7, game_count, replace = TRUE)
    hour_of_day <- sample(24, game_count, replace = TRUE)
    
    one_shot <- sample(c(FALSE, TRUE), game_count, replace = TRUE, prob = c(0.9, 0.1))
    
    names <- sample(names_df, game_count, replace = if(game_count > length(names_df)) {TRUE} else {FALSE})
    
    #Create Games
    games <- data.frame(
      game_id = game_id,
      game_name = names,
      
      experience = scores_experience,
      gm_rank = scores_ranking,
      
      combat = scores_combat,
      puzzle = scores_puzzles,
      social = scores_social,
      playerstory = scores_playerstory,
      roleplay = scores_roleplaying,
      rules = scores_rules,
      
      paid = scores_paid,
      
      dow = day_of_week,
      hod = hour_of_day,
      
      one_shot = one_shot
    )
    
    games
  }
  set.seed(22)
  games <- generate_games(10000)
  
  
